const OpenCC = {
  // 简化的简繁转换映射
  s2t: {
    '义': '義',
    '见': '見',
    '爱': '愛',
    '乐': '樂',
    '欢': '歡',
    '庆': '慶',
    '祷': '禱',
    '经': '經',
    '训': '訓',
    '话': '話',
    '说': '說',
    '讲': '講',
    '识': '識',
    '让': '讓',
    '认': '認',
    '这': '這',
    '个': '個',
    '们': '們',
    '为': '為',
    '时': '時',
    '会': '會',
    '来': '來',
    '过': '過',
    '发': '發',
    '与': '與'
  },
  
  t2s: {
    '義': '义',
    '見': '见',
    '愛': '爱',
    '樂': '乐',
    '歡': '欢',
    '慶': '庆',
    '禱': '祷',
    '經': '经',
    '訓': '训',
    '話': '话',
    '說': '说',
    '講': '讲',
    '識': '识',
    '讓': '让',
    '認': '认',
    '這': '这',
    '個': '个',
    '們': '们',
    '為': '为',
    '時': '时',
    '會': '会',
    '來': '来',
    '過': '过',
    '發': '发',
    '與': '与'
  },
  
  convert: function(text, mode) {
    if (mode === 's2t') {
      return text.split('').map(char => this.s2t[char] || char).join('');
    } else if (mode === 't2s') {
      return text.split('').map(char => this.t2s[char] || char).join('');
    }
    return text;
  }
};